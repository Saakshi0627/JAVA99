// To check whether thr character is in Upper case or in Lower case.


class UpperCaseChar
{

public static void main(String[]args)

{

char ch= 'a';

String output= (ch>=65 && ch<=90) ? ("The char is in Upper case.") : ("The char is in Lower case.");

System.out.println(output);

}

}


//Output: The char is in Lower case.
